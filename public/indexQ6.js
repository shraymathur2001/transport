function hotelApi()
{

    //displayHotels(hotelDetails);

    
    var hotelDetails=[];
    var cityName = document.getElementById('cityName').value;
    const params = {
        "type" : "hotel",
        "location" : cityName
    };

    //creating the query string from the parameters
    const queryString = new URLSearchParams(params).toString();
    console.log('queryString -->>' + queryString);


    //hitting the api
    const timeoutPromise = new Promise((resolve, reject) => {
        setTimeout(() => {
            reject(new Error('Timeout'));
        }, 6000); // 6 seconds
    });
    
    const fetchPromise = new Promise((resolve, reject) => {
        fetch(`${url}?${queryString}`, {
            method: 'GET',
            headers: headers
        })
        .then(async (res) => {
            if (res.ok) {
                console.log('API successful');
                const result = await res.json();
                resolve(result.results);
            } else {
                reject(new Error('No hotels'));
            }
        })
        .catch((err) => {
            reject(err);
        });
    });
    
    Promise.race([timeoutPromise, fetchPromise])
        .then((hotelDetails) => {
            console.log(hotelDetails);
            displayHotels(hotelDetails);
        })
        .catch((err) => {
            if (err.message === 'Timeout') {
                document.getElementsByClassName('form-group')[0].innerHTML = '';
                document.getElementById('forFav').innerHTML = 'No Hotels Found';
            } else {
                console.log('Error: ' + err.message);
            }
        });
}

async function displayMap(lat, long, name)
{
    console.log('LAT -->>  ' + lat);
    console.log('LONG -->> ' + long);

    var map = L.map('map').setView([lat, long], 13);
    
    var myMap = await L.tileLayer('https://tile.openstreetmap.org/{z}/{x}/{y}.png', {
        maxZoom: 19,
        attribution: '&copy; <a href="http://www.openstreetmap.org/copyright">OpenStreetMap</a>'
    }).addTo(map)
    
    var marker = L.marker([lat, long]).addTo(map);
    marker.bindPopup(`${name}`).openPopup();
    document.getElementById('popup').style.display = 'block';
    

}

